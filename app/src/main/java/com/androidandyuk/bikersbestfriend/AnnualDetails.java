public class AnnualDetails {
    int afYear;
    int afCount;
    Double afCost;
    Double afMiles;
    Double afLitres;

    public AnnualDetails(int afYear, int afCount, Double afCost, Double afMiles, Double afLitres) {
        this.afYear = afYear;
        this.afCount = afCount;
        this.afCost = afCost;
        this.afMiles = afMiles;
        this.afLitres = afLitres;
    }
}
